def add_food(db, cats, seasons, name_t, name_e, cat_key, months,
             img, desc, cals, prot, carbs, fat, fib, vit, min_,
             benefits, recipes):
    """Helper to create a food with all related data"""
    food = Food(
        name_tamil=name_t,
        name_english=name_e,
        category_id=cats[cat_key].id,
        description_tamil=desc,
        image_url=img,
    )
    db.add(food)
    db.flush()

    # Seasons
    for m in months:
        food.seasons.append(seasons[m])

    # Nutrition
    nut = Nutrition(food_id=food.id, calories=cals, protein=prot,
                    carbohydrates=carbs, fat=fat, fiber=fib,
                    vitamins=vit, minerals=min_)
    db.add(nut)

    # Benefits
    for b in benefits:
        db.add(HealthBenefit(food_id=food.id, benefit_tamil=b))

    # Recipes (list of dicts)
    for r in recipes:
        db.add(Recipe(
            food_id=food.id,
            title_tamil=r["title"],
            ingredients_tamil=r["ing"],
            instructions_tamil=r["steps"],
            prep_time=r.get("prep", "10 நிமிடம்"),
            cook_time=r.get("cook", "20 நிமிடம்"),
        ))

    return food


